//1. Arithmetic Operators

let x;
x = 5 + 5;
x = 5 - 5;
x = 93 * 93;
x = 5 / 5;
x = 7 % 5; //remainder

//Concatenation
x = 'Hello' + ' ' + 'World';

//Exponent
x = 2 ** 3;

//Increment
x = 1;
// x = x + 1;
x++;

//Decrement
// x = x - 1;
x--;

//2. Assignment Operators
x = 10;

x += 5; //x = x + 5
x -= 5;
x *= 5;
x /= 5;
x %= 5;
x **= 5;

// 3. Comparison Operators

//Equal to  (just the Value, not the type)
x = 2 == '2'; //loose equality

//Equal to (type and value)
x = 2 === '2'; //strict equality

//Not equal to ( Just the value, bot the type)
x = 2 != '2';

//Not equal to (the value and the type)
x = 2 !== '2';

//Greater than and less than

x = 10 > 5;
x = 10 < 5;
x = 10 <= 5;
x = 10 >= 5;

